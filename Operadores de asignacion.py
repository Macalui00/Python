#operadores de asignacion: se utilizan para asignar valores a variables

numero = 5 #el igual es un operador de asignacion
print(numero)

#+=
#numero = numero + 4
#es equivalente a:
numero +=4
print(numero)

#lo mismo para una resta
#numero = numero - 3
#es equivalente a:
numero -= 3
print(numero)

#esto mismo sucede para multiplicacion, division y exponente
#numero = numero * 4
#equivalente a:
numero *= 4
print(numero)

#numero = numero / 4
#equivalente a:
numero /= 4
print(numero) # como es una division nos pone el numero de tipo float, con decimales.

#para exponente:
numero = 2
#numero = numero ** 3
#equivalente a:
numero **= 3
print(numero)
