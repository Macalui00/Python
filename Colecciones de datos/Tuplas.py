#Tuplas: coleccion de elementos ordenadas que es inalterable, es decir no se puede modificar.

tuplas_colores = ("rojo", "verde", "amarillo") #lo colocamos entre parentesis para indicar que es una tupla.

print(tuplas_colores)

for color in tuplas_colores:
	print(color)

print(tuplas_colores[2])

#En una tupla no se pueden añadir más elementos, ni se pueden borrar.
#Es una coleccion de elementos que no se pueden modificar.

print(len(tuplas_colores))