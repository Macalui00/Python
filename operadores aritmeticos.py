#operadores aritmeticos (+,-,*,/,%.**,//)

numero1 = 10
numero2 = 5

suma = numero1 + numero2
print(suma)

resta = numero1 - numero2
print(resta)

multiplicacion = numero1 * numero2
print(multiplicacion)

division = numero1 / numero2
print(division)

numero1 = 5
numero2 = 2
division = numero1 / numero2
print(division) #numero decimal

#cual es el cociente de la division y cual es el resto de la division:
#cociente 2, resto 1

#operador para el resto %
resto = numero1 % numero2
print(resto) 

#operador para cociente //
cociente = numero1 // numero2
print(cociente)

#operacion de exponente, ejemplo 2 elevado a la 3:
exponente = 2 ** 3 #seria dos veces la multiplicacion
print(exponente)